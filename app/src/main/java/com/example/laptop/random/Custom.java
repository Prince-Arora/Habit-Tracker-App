public class Custom {
    public String a;
    public String b;
    public String c;
    public Custom(String x, String y, String z)
    {
        a=x;
        b=y;
        c=z;
    }
    public String GETA()
    {
        return a;
    }
    public String GETB()
    {
        return b;
    }
    public String GETC()
    {
        return c;
    }

}
